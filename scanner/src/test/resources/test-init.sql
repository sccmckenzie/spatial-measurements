create schema raw_measurements;
create schema config;
create sequence raw_measurements.scan_id_sequence start with 1 increment by 1;

create table raw_measurements.measurement
(
    measurement_id    bigint generated by default as identity primary key,
    measurement_value double precision not null,
    scan_id           bigint,
    x                 integer,
    y                 integer,
    modified_at       timestamp default current_timestamp
);

create table config.grid
(
    id          integer primary key,
    template_id integer,
    x           integer,
    y           integer
);

